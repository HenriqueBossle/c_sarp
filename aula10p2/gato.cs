public class Gato
{
    public string nome;
    public int idade;

    public Gato(string nome, int idade)
    {
        this.nome = nome;
        this.idade = idade;
    }

    public Gato = (string nome)
    {
        this.nome = nome;
        idade = 0;
    }

    public Gato()
    {
        nome = "Desconhecido";
        idade = 0;
    }


}